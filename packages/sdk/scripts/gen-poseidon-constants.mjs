// Extracts poseidon255 round constants C(t) and MDS matrices M(t) for t = 2,3,4 from the
// Circom constants file and emits a JS module (+ .d.ts). Run: node scripts/gen-poseidon-constants.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(here, "../../../circuits/src/lib/poseidon255_constants.circom");
const OUT_JS = resolve(here, "../src/poseidon-constants.js");
const OUT_DTS = resolve(here, "../src/poseidon-constants.d.ts");

const text = readFileSync(SRC, "utf8");

const mIdx = text.indexOf("function MATRIX");
if (mIdx < 0) throw new Error("MATRIX function not found");
const constText = text.slice(text.indexOf("function CONSTANTS"), mIdx);
const matrixText = text.slice(mIdx);

// Each `if (t == N) { return [...] }` branch may span lines; split on branch boundaries.
function hexTokensForT(region, t) {
  const parts = region.split(/if\s*\(\s*t\s*==\s*/).slice(1);
  const part = parts.find((p) => /^\s*(\d+)/.exec(p)?.[1] === String(t));
  if (!part) throw new Error(`no branch for t == ${t}`);
  const toks = part.match(/0x[0-9a-fA-F]+/g);
  if (!toks) throw new Error(`no hex for t == ${t}`);
  return toks.map((h) => BigInt(h));
}

const C = {};
const M = {};
for (const t of [2, 3, 4]) {
  const c = hexTokensForT(constText, t);
  if (c.length !== t * 64) throw new Error(`C(${t}) length ${c.length} != ${t * 64}`);
  const mflat = hexTokensForT(matrixText, t);
  if (mflat.length !== t * t) throw new Error(`M(${t}) length ${mflat.length} != ${t * t}`);
  const rows = [];
  for (let i = 0; i < t; i++) rows.push(mflat.slice(i * t, i * t + t));
  C[t] = c;
  M[t] = rows;
}

const fmt = (arr) => "[" + arr.map((x) => `${x}n`).join(", ") + "]";
const fmtM = (rows) => "[" + rows.map((r) => fmt(r)).join(", ") + "]";

const header =
  "// AUTO-GENERATED by scripts/gen-poseidon-constants.mjs — do not edit by hand.\n" +
  "// poseidon255 round constants and MDS matrices for state sizes t = 2, 3, 4 (BLS12-381 Fr).\n\n";

const js = [header];
js.push("export const POSEIDON_C = {");
for (const t of [2, 3, 4]) js.push(`  ${t}: ${fmt(C[t])},`);
js.push("};", "");
js.push("export const POSEIDON_M = {");
for (const t of [2, 3, 4]) js.push(`  ${t}: ${fmtM(M[t])},`);
js.push("};", "");
writeFileSync(OUT_JS, js.join("\n"));

const dts =
  header +
  "export declare const POSEIDON_C: Record<number, bigint[]>;\n" +
  "export declare const POSEIDON_M: Record<number, bigint[][]>;\n";
writeFileSync(OUT_DTS, dts);

console.log(`wrote ${OUT_JS} and ${OUT_DTS}`);
for (const t of [2, 3, 4]) console.log(`  t=${t}: C=${C[t].length} M=${M[t].length}x${M[t][0].length}`);
